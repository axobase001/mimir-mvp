def test_serialization_roundtrip_preserves_origin():
    """Save/load state should preserve goal origin."""
    config = make_config()
    bg = BeliefGraph(config)
    sec = SECMatrix(config)
    mem = Memory(config)

    goals = {
        "g1": Goal(
            id="g1", target_belief_id="b1",
            description="auto goal", reason="high PE",
            origin=GoalOrigin.ENDOGENOUS,
            created_at=1, priority=0.5,
        ),
        "g2": Goal(
            id="g2", target_belief_id="b2",
            description="user goal", reason="User requested",
            origin=GoalOrigin.EXOGENOUS,
            created_at=2, priority=0.8,
        ),
    }

    with tempfile.TemporaryDirectory() as tmpdir:
        state_path = str(Path(tmpdir) / "state.json")
        MimirState.save(state_path, bg, sec, mem, goals, 5, {})

        _, _, _, loaded_goals, _, _ = MimirState.load(state_path, config)

    assert loaded_goals["g1"].origin == GoalOrigin.ENDOGENOUS
    assert loaded_goals["g2"].origin == GoalOrigin.EXOGENOUS


def test_backward_compat_missing_origin_defaults_endogenous():
    """Old state without origin field should default to ENDOGENOUS."""
    config = make_config()
    data = {
        "cycle_count": 5,
        "belief_graph": {"nodes": {}, "edges": [], "counter": 0},
        "sec_matrix": {"entries": {}},
        "memory": {"episodes": [], "procedures": {}},
        "goals": {
            "g1": {
                "id": "g1",
                "target_belief_id": "b1",
                "description": "old goal",
                "reason": "test",
                "status": "active",
                "created_at": 1,
                "priority": 0.5,
                # No "origin" key
            }
        },
        "usage_stats": {},
    }

    _, _, _, goals, _, _ = MimirState.load_from_dict(data, config)
    assert goals["g1"].origin == GoalOrigin.ENDOGENOUS


def test_preference_belief_does_not_trigger_goal():
    """PREFERENCE category beliefs with high PE should NOT trigger investigate goals."""
    from mimir.types import BeliefCategory

    config = make_config()
    bg = BeliefGraph(config)
    sec = SECMatrix(config)
    gg = GoalGenerator(config, bg, sec)

    # PREFERENCE belief with high PE
    b = Belief(
        id="b1", statement="I prefer dark mode",
        confidence=0.7, source=BeliefSource.OBSERVATION,
        created_at=0, last_updated=5, last_verified=5,
        pe_history=[0.5, 0.5, 0.5],
        tags=["ui_preference"],
        category=BeliefCategory.PREFERENCE,
    )
    bg.add_belief(b)

    goals = gg.generate_goals(current_cycle=6)
    # Should NOT generate goal for PREFERENCE belief
    investigate_goals = [g for g in goals if "Investigate" in g.description]
    assert len(investigate_goals) == 0


