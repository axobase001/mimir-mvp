"""Tests for goal origin system (Change #4).

Verifies:
- Auto-generated goals are ENDOGENOUS
- Default goal origin is ENDOGENOUS
- EXOGENOUS goals are not auto-abandoned
- Serialization roundtrip preserves origin
"""

import json
import tempfile
from pathlib import Path

from mimir.brain.belief_graph import BeliefGraph
from mimir.brain.sec_matrix import SECMatrix
from mimir.brain.goal_generator import GoalGenerator
from mimir.types import Belief, BeliefSource, Goal, GoalOrigin, GoalStatus, SECEntry
from mimir.config import MimirConfig
from mimir.state import MimirState
from mimir.brain.memory import Memory


def make_config() -> MimirConfig:
    return MimirConfig(
        goal_pe_threshold=0.3,
        goal_pe_persistence=3,
        goal_staleness_threshold=10,
        max_active_goals=5,
    )


def test_default_goal_origin_is_endogenous():
    """New Goal without explicit origin should be ENDOGENOUS."""
    g = Goal(
        id="g1", target_belief_id="b1",
        description="test", reason="test",
    )
    assert g.origin == GoalOrigin.ENDOGENOUS


def test_auto_generated_investigate_goal_is_endogenous():
    """Goals generated by GoalGenerator from high PE should be ENDOGENOUS."""
    config = make_config()
    bg = BeliefGraph(config)
    sec = SECMatrix(config)
    gg = GoalGenerator(config, bg, sec)

    b = Belief(
        id="b1", statement="test belief",
        confidence=0.7, source=BeliefSource.OBSERVATION,
        created_at=0, last_updated=5, last_verified=5,
        pe_history=[0.5, 0.4, 0.6, 0.5],
        tags=["test"],
    )
    bg.add_belief(b)

    goals = gg.generate_goals(current_cycle=6)
    assert len(goals) == 1
    assert goals[0].origin == GoalOrigin.ENDOGENOUS


